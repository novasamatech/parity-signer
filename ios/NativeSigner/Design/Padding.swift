import UIKit

enum Padding {
    /// No padding: 0 pts
    static let none: CGFloat = 0
    /// Extra Extra Small: 4 pts
    static let extraExtraSmall: CGFloat = 4
    /// Extra Small: 8 pts
    static let extraSmall: CGFloat = 8
    /// Small: 12 pts
    static let small: CGFloat = 12
    /// Medium: 16 pts
    static let medium: CGFloat = 16
    /// Large: 24 pts
    static let large: CGFloat = 24
}
